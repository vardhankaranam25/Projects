    public void populateGameRequestTable() {
        ArrayList<GameRoomServiceRequest> pendingGameRequest = new ArrayList<GameRoomServiceRequest>();
        ArrayList<GameRoomServiceRequest> non_pendingGameRequest = new ArrayList<GameRoomServiceRequest>();

        for (GameRoomServiceRequest gameWRequest : system.getGameRoomServiceRequestList().getGameWorkRequestList()) {
            if (gameWRequest.getStatus().equals("Pending")) {
                pendingGameRequest.add(gameWRequest);
            } else {
                non_pendingGameRequest.add(gameWRequest);
            }
        }

        populate_based_on_status(pendingGameRequest, non_pendingGameRequest);
    }

    public void populate_based_on_status(ArrayList<GameRoomServiceRequest> pendingGameRequest, ArrayList<GameRoomServiceRequest> non_pendingGameRequest) {
        DefaultTableModel model = (DefaultTableModel) tblGameRequests.getModel();
        model.setRowCount(0);
        for (GameRoomServiceRequest gameWRequest : pendingGameRequest) {

            Object[] newRow = new Object[7];
            newRow[0] = gameWRequest;
            newRow[1] = gameWRequest.getGameDetails().getGameName();
            newRow[2] = gameWRequest.getGameDetails().getGameCategory();
            newRow[3] = gameWRequest.getGameDetails().getGameprice();
            newRow[4] = gameWRequest.getNumberOfHours();
            newRow[5] = gameWRequest.getBookingDate();
            newRow[6] = gameWRequest.getStatus();

            model.addRow(newRow);
        }

        DefaultTableModel modelNon_Pending = (DefaultTableModel) tblGameConfirmed.getModel();
        modelNon_Pending.setRowCount(0);
        for (GameRoomServiceRequest gameWRequest : non_pendingGameRequest) {

            Object[] newRow = new Object[7];
            newRow[0] = gameWRequest;
            newRow[1] = gameWRequest.getGameDetails().getGameName();
            newRow[2] = gameWRequest.getGameDetails().getGameCategory();
            newRow[3] = gameWRequest.getGameDetails().getGameprice();
            newRow[4] = gameWRequest.getNumberOfHours();
            newRow[5] = gameWRequest.getBookingDate();
            newRow[6] = gameWRequest.getStatus();

            modelNon_Pending.addRow(newRow);
        }

    }
    
    public void updateWorkRequestStatus(GameRoomServiceRequest selectedGame, String status) {
        selectedGame.setStatus(status);
        int index = 0;
        for (GameRoomServiceRequest gameWRequest : system.getGameRoomServiceRequestList().getGameWorkRequestList()) {
            if (gameWRequest.getCustomerId().equals(selectedGame.getCustomerId())) {
                system.getGameRoomServiceRequestList().updateGameWorkRequest(gameWRequest, index);
                break;
            }
            index++;
        }
    }

