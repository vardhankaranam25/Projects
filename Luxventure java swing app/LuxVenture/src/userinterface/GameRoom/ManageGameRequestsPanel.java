    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
         try {
            int selectedRowIndex = tblGameRequests.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to approve.");
                return;
            }
            DefaultTableModel model = (DefaultTableModel) tblGameRequests.getModel();
            GameRoomServiceRequest selectedGameWorkRequest = (GameRoomServiceRequest) model.getValueAt(selectedRowIndex, 0);
            updateWorkRequestStatus(selectedGameWorkRequest, "Approved");
            CustomerPayments ct = new CustomerPayments();
            
            //User definition for email notif
            Customer getUser = system.getCustomerList().getCustomerById(selectedGameWorkRequest.getCustomerId());

            float gamebooking_finalprice = selectedGameWorkRequest.getNumberOfHours() * selectedGameWorkRequest.getGameDetails().getGameprice();
            ct.setCustomerId(selectedGameWorkRequest.getCustomerId());
            ct.setFacilityUsed("Game Booked - " + selectedGameWorkRequest.getGameDetails().getGameName() + " for " + selectedGameWorkRequest.getNumberOfHours() + " hours");
            ct.setCheckoutprice(gamebooking_finalprice);
            system.getCustomerPaymentsList().addCustomerPayment(ct);
            
             //email notification
            new EmailAlert().sendEmail(getUser, "Game",true);
            
            JOptionPane.showMessageDialog(this, "Request approved successfully and email notification sent");
            populateGameRequestTable();
            formatRows();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnApproveActionPerformed

